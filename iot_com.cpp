Iot_Com::Iot_Com(uint8_t addr, uint16_t pin_sda, uint16_t pin_clk)
{
    this->i2c_addr = addr;
    Wire.setClock(10000);
    Wire.begin(pin_sda, pin_clk);    
    init();
}
/******************************************************/
void Iot_Com::init()
{
    key_list.clear();
    authority_check = false;
}
